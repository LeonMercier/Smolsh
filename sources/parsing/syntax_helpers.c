#include "minishell.h"

size_t	skip_quotes(char *str)
{
	size_t	i;

	i = 0;
	if (str[i] == '\'')
	{
		i++;
		while (str[i] != '\"' && str[i] != '\0')
			i++;
	}
	if (str[i] == '\"')
	{
		i++;
		while (str[i] != '\'' && str[i] != '\0')
			i++;
	}
	return (i);
}
